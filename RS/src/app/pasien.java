    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String pasienId = txtID.getText();
        String nama = txtNama.getText();
        String jk = null;
        if (RDL.isSelected()) {
            jk="laki-laki";
        }else if (RDP.isSelected()) {
            jk="perempuan";
        }
        String umur = txtUmur.getText();
        String sakit = txtSakit.getText();
        String ruang = txtRuang.getText();
        String alamat = txtAlamat.getText();
        
            try {
                Connection c = koneksi.MySQL();
                String q = "UPDATE pasien "
                        + "SET nama_lengkap='"+nama+"',"
                        + "umur='"+umur+"',"
                        + "jenis_kelamin='"+jk+"',"
                        + "sakit='"+sakit+"',"
                        + "ruang='"+ruang+"',"
                        + "alamat='"+alamat+"'"
                        + "WHERE id_pasien="+pasienId+"";
                Statement st =c.createStatement();
                st.executeUpdate(q);
            JOptionPane.showMessageDialog(null, "data berhasil di edit");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Perubahan Data Gagal"+ e.getMessage());
        }
        
        refresh();
        txtID.setEnabled(true);
        aktif(false);
        setTombol(true);
        HalamanUtama.panggilPanel(new pasien());
        loadData("");
            
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int baris = jTable1.getSelectedRow();
        if (baris != -1) {
            int pilih = JOptionPane.showConfirmDialog(this,
                    "Apakah Anda Yakin ?",
                    "Konfirmasi",
                    JOptionPane.YES_NO_OPTION);
            if (pilih == 0 ) {
                try {
                    Connection c = koneksi.MySQL();
                    Statement st = c.createStatement();
                    Object oid=jTable1.getValueAt(baris, 2);
                    String strid = oid.toString();
                    int id = Integer.parseInt(strid);
                    String q = "DELETE FROM pasien WHERE id_pasien="+id+"";
                    st.executeUpdate(q);
                    loadData("");
                    JOptionPane.showMessageDialog(this, " Data telah dihapus!");
                } catch (HeadlessException | NumberFormatException | SQLException e) {
                }
            } else if (pilih == 1) {
                jTable1.clearSelection();
            }
        }
        HalamanUtama.panggilPanel(new pasien());
        refresh();
        txtID.setEnabled(true);
        aktif(false);
        setTombol(true);
        loadData("");
    }//GEN-LAST:event_btnHapusActionPerformed

