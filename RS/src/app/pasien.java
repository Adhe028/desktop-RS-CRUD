    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String userId = jTable1.getValueAt(baris, 2).toString();
        txtID.setText(userId);
        String nama =jTable1.getValueAt(baris, 3).toString();
        txtNama.setText(nama);
        String jk = jTable1.getValueAt(baris, 4).toString();
        if (jk.equals("laki-laki")) {
            RDL.setSelected(true);
                
        }else  {
            RDP.setSelected(true);
                
        }
        String umur = jTable1.getValueAt(baris,5).toString();
        txtUmur.setText(umur);
        String ruang = jTable1.getValueAt(baris, 6).toString();
        txtRuang.setText(ruang);
        String sakit = jTable1.getValueAt(baris, 7).toString();
        txtSakit.setText(sakit);
        String alamat =jTable1.getValueAt(baris, 8).toString();
        txtAlamat.setText(alamat);

        
        txtID.setEnabled(false);
        aktif(true);
        btnCari.setEnabled(false);
        btnTambah.setEnabled(false);
        btnBatal.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

