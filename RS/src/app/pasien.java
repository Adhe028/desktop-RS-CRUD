    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String nama = txtNama.getText();
        String umur = txtUmur.getText();
        String ruang = txtRuang.getText();
        String sakit = txtSakit.getText();
        String alamat = txtAlamat.getText();
        String jk = null;
        if (RDL.isSelected()) {
            jk="laki-laki";
        }else if (RDP.isSelected()) {
            jk="perempuan";
        }

        if(nama.isEmpty()){
            JOptionPane.showMessageDialog(this,"Nama Harus Diisi");
            txtNama.requestFocus();
        }else if (umur.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Umur Harus Diisi");
            txtRuang.requestFocus();
        }else if (ruang.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Ruang Harus Diisi");
            txtRuang.requestFocus();
        }else{if (sakit.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Sakit Harus Diisi");
            txtSakit.requestFocus();
        }else if (alamat.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Alamat Harus Diisi");
            txtAlamat.requestFocus();
        }else    
            try {
                Connection c = koneksi.MySQL();
                Statement st = c.createStatement();
                String q = "INSERT INTO pasien (nama_lengkap,umur,sakit,jenis_kelamin,ruang,alamat) VALUES ('"+nama+"','"+umur+"','"+sakit+"','"+jk+"','"+ruang+"','"+alamat+"')";
                st.executeUpdate(q);
                JOptionPane.showMessageDialog(this, "Data pasien"
                        + " Berhasil Disimpan");
            } catch ( SQLException e ) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        refresh();
        HalamanUtama.panggilPanel(new pasien());
        }
        
    }//GEN-LAST:event_btnSimpanActionPerformed

