    private void loadData (String key){
         try {
             model.setRowCount(0);
             String where = !key.isEmpty() ? "WHERE id_pasien LIKE '%"+key+"%' OR nama_lengkap LIKE '%"+key+"%' OR jenis_kelamin LIKE '%"+key+"%' OR umur LIKE '%"+key+"%' OR ruang LIKE '%"+key+"%' OR sakit LIKE '%"+key+"%' OR alamat LIKE '%"+key+"%' " : "";
             String q = "SELECT * FROM pasien "+where;
             Connection c = koneksi.MySQL();
             Statement st = c.createStatement();
             ResultSet rs = st.executeQuery(q);
             int nomor = 0;
             while (rs.next()) {
                 int id = rs.getInt("id_pasien");
                 String nama = rs.getString("nama_lengkap");
                 String jk = rs.getString("jenis_kelamin");
                 String umur = rs.getString("umur");
                 String ruang = rs.getString("ruang");
                 String sakit = rs.getString("sakit");
                 String alamat = rs.getString("alamat");
                         nomor++;
                         Object[] data = {"",nomor, id, nama, jk, umur, ruang, sakit, alamat};
                         model.addRow(data);    
            }
         } catch (Exception e) {
         }
    }

